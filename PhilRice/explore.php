<h4>Transformations in Progress</h4>
<p class="text-justify">In this exhibit, we celebrate the transitions that occurred in the rice fields since the Spaniards took records of what they have witnessed on rice production. That for us to understand the advances on agriculture today, we must know what were the contexts contributing to these changes. The snippet of stories leading to modern farming are presented through arts and in the cultural materials. The exhibit also highlight climate-ready varieties and practices that help farmers adapt to adversaries such as ecological engineering, and diversified farming.<br>
