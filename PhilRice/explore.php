<h2>Major theme exhibits</h2>
<h4>Lovelife with Rice</h4>
<p>Lovelife with Rice shows the intimate connection between a farmer, research and development worker, and the ordinary passerby with rice.  The collections tell about the struggles of humans – from the establishment of the Ifugao Rice Terraces, green revolution, until the development of modern technologies, and how these agricultural events improved our relationship with rice.</p>

<h4>Bountiful Harvest</h4>
<p>The exhibit features the practices and technologies that brought abundance to traditional and modern farming. It highlights cultural materials of post-harvest technologies from Nueva Ecija, modern post-harvest machinery, and a feature on rice varieties from different regions of the country. The museum also display a gallery of photographs featuring the role of women in rice farming.</p>

<h4>Colors of Rice</h4>
<p>Colors of Rice shows the impact of rice on our health, in our life, and in our communities. It highlights the different pigments of rice—some are products of science and some have been long part of our rice material culture.</p>
<p>It highlights the nutritional value of colored rice in comparison to white rice. Brown, purple, red, black, and golden rice are displayed for the audience to ponder and compare.</p>
<p>Colors of Rice also honors what rice signifies to the people who plant and eat it. A collection of photographs features interpretations on the how rice adds color to the communities, and to life, per se. In addition, it features the Pahiyas Festival of Quezon, which highlights the colorful kiping (a rice wafer).</p>
<hr>

<h2>Special exhibit</h2>

<h4>Palay Kamalayan</h4>
<p>The exhibit utilizes art to promote education on rice science. It features works of about 25 artists based in the cities of Baguio, Quezon, and Los Baños, with rice as their medium and subject.</p>

 
<h4>Botong Francisco: A Nation Imagined</h4>
<p>An art exhibit with the Ayala Museum, it houses 25 painting reproductions of the Philippine’s second national artist.  It shows how Botong Francisco tie up the cycle of life in a rural town in the 1940s and 1950s through themes of hometown, the land, and celebration. It shows the joyful and symbiotic relationship of man and nature in ensuring progeny and produce, and a life of peace and abundances.</p>
<p>The exhibit aims to raise awareness on the culture of rice and the social issues affecting it and was hoped to encourage the youth to think of their role in feeding the nation.</p>
	
