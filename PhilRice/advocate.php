<h4>Rice Science Exploratorium</h4>
<p>Help us fund The Rice Science Exploratorium – the larger goal of establishing the Rice Science Museum. The Exploratorium is envisioned to be the premier institution and repository of Philippine cultural heritage in rice, a platform to disseminate interest in rice science and technology to the general public. It will be a place to explore the richness of rice culture in Philippine development, inspire generations to take part in this core industry, and contribute to a self-sufficient, sustainable, and competitive rice economy through the promotion and preservation of rice science and culture. It will house galleries about rice breeding, varieties, and rice ecology, among other rice complexities.</p>

<h4>Friends of the Rice Science Museum</h4>
<p>Members of the Rice Science Museum promote the museum and support its activities. They serve as volunteers in organizing, facilitating, and implementing exhibits, workshops, and fundraising and social events.</p>
<p>As members, the enjoy enrichment opportunities such as docent training, art and cultural workshops, and study tours. For membership, email us at <a href="mailto:ricesciencemuseum@gmail.com">ricesciencemuseum@gmail.com.</a></p>

<h4>Partners</h4>
<p>To increase our visibility and wider our reach, the museum cultivated partnership with the National Committee of Museums, Central Luzon Association of Museums, UPLB Museum of Natural History, and Museo Pambata. We have collaborated with the Ayala Foundation in hosting the Botong Francisco: A Nation Imagined in which the 25 painting reproductions of the country’s second national artist were exhibited. During the exhibit launch, about 20 municipal tourism officers led by   Cristina Wycoco-Paulino, signified their commitment in including the museum in their campaigns.</p>
<p>The provincial government of Ilocos Norte and the museum worked on developing the Balay Dingras, which was one of the three museums launched on November 19 as part of the province’s initiatives to boost its Paoay Kumakaway! tourism campaign. With a concept resembling a rice miller’s house, Balay Dingras features collections from the Rice Science Museum.</p>

