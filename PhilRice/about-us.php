	<h3>Rice Science Museum</h3>
	The Rice Museum of the Philippine Rice Research Institute (PhilRice), was established in 2004 to raise awareness on rice culture. However, the museum focused on few collections about the Cordillera, limiting rice appreciation in the highlands. 
<br><br>
With its renovation in Sept. 3, 2014, 10 years after it was first launched, major curatorial updating was conducted and was relaunched as Rice Science Museum.  
A facility accredited by the Department of Tourism, the 320-square-meter building, which was the original administration building of the agency, features thematic displays that are bi-annually updated. 
<br><br>
At the museum, you will not only appreciate the 2,000-year journey of rice, but will also understand the complexity and the seemingly simplicity of rice through its changing theme.
<br><br>
