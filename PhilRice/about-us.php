<h3>Impact on Food Security</h3>
After the Rice Science Museum was relaunched on Sept. 2014, we are helping sustain a competitive rice economy through the following:
<br><br>
<ul>
<li><b>Helping ensure the food security</b><br>
Our evaluation of the Rice Science and Art Summer Camp showed that most of the participants, aged 9-17 years old, had resolved to consider working in the agriculture sector. Feedback also showed that the 8 to 12 year olds attendees of the Paglaki ko, Gusto kong maging Agricultural Scientist perceived that it is important for them to know about agricultural scientist as career path to sustain food production. 
</li>
<br>
The Rice Science Museum is a good way to raise public awareness about the nation’s staple. But if we want to ensure food security, a more aggressive effort is needed to encourage Filipinos to pursue a career in rice farming and other agricultural pursuits. – Editorial, The Philippine Star (August 16, 2015)
<br>

<li><b>Encouraging social action</b><br>
After exposure to the mobile museum exhibits, audience learned and appreciated the value of rice and committed that they will no longer waste a single grain of rice.  Schools in Metro Manila also expressed willingness to host the mobile museum implying its potential in molding desirable behavior towards rice self-sufficiency.
</li>
<br>
<li><b>Furthering adoption of technologies</b><br>
Through the transitory and mobile exhibits, our evaluation showed that visitors had demonstrated the ability to use the information from the display to make conclusions or decisions.</li>
<br>
<li><b>Mobilizing knowledge</b><br>
In our study titled, Interactive Learning Behavior: The Rice Science Museum Experience, we found that farmers gain knowledge to improve their skill and express interest in sharing the information acquired from the museum.</li>
<br>
<li><b>Placing PhilRice and the Science City of Muñoz in the tourism industry</b><br>
The Department of Tourism has declared the Rice Science Museum as tourist spot and as a local government unit accredited establishment. The only other accredited museum in Region III is the Aquino Center and Museum in Tarlac City.</li>

</ul>

	
