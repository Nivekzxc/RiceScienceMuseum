Our regional museums
<br><br>
We promote cultural heritage and awareness on rice in the Northeastern and Northwestern Luzon.
<br><br>
The Rice Science Museum in Batac City, Ilocos Norte features the confluence between the Ilocos and Cordillera. It showcases the traditional and present practices in farming, modern rice varieties, and the rice landscape in the two regions.
<br><br>
On the other hand, our museum in San Mateo, Isabela highlights the co-existence of the traditional and modern farming practices implemented by farmers in the Cordillera and Region II and the traditional and hybrid rice. The concurrence of the old and new ways make Region II as one of the country’s top rice-producing provinces.
